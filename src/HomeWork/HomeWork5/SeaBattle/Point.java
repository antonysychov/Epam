package HomeWork.HomeWork5.SeaBattle;

public class Point {
    int x;
    int y;
    boolean isFilled;

    public Point(int x, int y, boolean isFilled) {
        this.x = x;
        this.y = y;
        this.isFilled = isFilled;
    }

    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }
}
